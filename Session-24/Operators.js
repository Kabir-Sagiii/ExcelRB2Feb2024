var x = 200;

var y = 20;

console.log(x + y);

console.log(x - y);

console.log(x * y);

console.log(x / y);

console.log(x % y); // 200 / 20 ==> remainder

console.log(x ** 2);

console.log(x++); //x = x + 1 //Post Increment

console.log(++x); //x = x + 1  // Pre Increment

console.log(x--); // x = x -1  // Post Decrement

console.log(--x); // x = x -1 //Pre Decrement
